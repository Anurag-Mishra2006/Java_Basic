public class Loops {
    
    public static void main(StringLearning args[]){

        for(int i = 0; i < 10; i++){
            System.out.print("Hello, Anurag Bro..."+"\n");
        }

        int i = 0;
        while(i < 5){
            System.out.print(i+ " ");
            i++;
        }
        System.out.println();

        do{
            System.out.println("it run the code atleast one time");
            i++;
        }while(i < 5);

    }
}
