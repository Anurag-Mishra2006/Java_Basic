public class ifelse {
    public static void main(StringLearning []args){

        Scanner sr = new Scanner(System.in);
        int a = sr.nextInt();

        System.out.println(Math.max(5,Math.max(6,9)));
        System.out.println((a >= 18? "You can drive": "You can't drive."));

    }
}
